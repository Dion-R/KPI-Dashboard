// tiers
const tiers = [44, 54, 64, 84, 100];

// leads Until
const leadsUntilHeading = document.getElementById("leadsUntilHeading");

const tierDeterminer = function () {
  let count = 0;
  for (const tier of tiers) {
    if (activeLeadsCount >= tier) {
      count++;
    }
  }
  return count;
};
const tier = tierDeterminer();

function nextTarget() {
  if (tier < tiers.length) {
    const target = tiers[tier];
    const progress = target - activeLeadsCount;
    return progress;
  }
}
const leadsRequired = nextTarget();

function setText() {
  const leadsUntilHeading = document.getElementById("leadsUntilHeading");
  const leadsUntil = document.getElementById("leadsUntil");

  if (tier < tiers.length) {
    leadsUntilHeading.innerText = `Leads until Level ${tier + 1}`;
    leadsUntil.innerText = `${leadsRequired}`;
  } else {
    leadsUntilHeading.innerText = `You're on the max Tier`;
    leadsUntil.innerText = `Congratulations`;
  }

  const tierLevel = document.getElementById("currentLevel");
  tierLevel.innerText = tier;
}
setText();

function commissionPayable() {
  const payouts = [30, 30, 32, 34, 36, 40];
  const payablePerLead = payouts[tier];
  const payable = activeLeadsCount * payablePerLead;
  const comissionPayable = document.getElementById("comissionPayable");
  comissionPayable.innerText = `$${payable}.00`;

  const commissionPayableTitle = document.getElementById(
    "commissionPayableTitle"
  );

  if (tier === 0) {
    commissionPayableTitle.innerText = "Commission unlocked at Level 1";
  } else {
    commissionPayableTitle.innerText = "Commission unlocked";
  }
}
commissionPayable();
