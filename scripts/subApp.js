function loadLeads(data) {
  const leads = data
    .map((lead) => {
      return `<div class="d-grid individualLead bg-white p-2">
          <p class="mb-0">${lead.name}</p>
          <p class="mb-0">${lead.date}</p>
          <p class="mb-0">${lead.status}</p>
          <p class="mb-0">${lead.health}</p>
          <p class="mb-0">${lead.progress}</p>
          <p class="mb-0">${lead.commission}</p>
        </div>`;
    })
    .join("");
  leadContainer.innerHTML = leads;
}

// Change the toggled styles and filter the data set to show active or Dead Leads when they are selected.
const tabs = document.getElementsByClassName("tabs");
for (const tab of tabs) {
  tab.addEventListener("click", (e) => {
    for (const tab of tabs) {
      tab.classList.remove("selected");
      e.target.classList.add("selected");
    }

    const filterValue = e.target.id;
    if (filterValue === "all") {
      loadLeads(company);
    } else {
      const leads = company.filter((lead) => lead.health === filterValue);
      loadLeads(leads);
    }
  });
}

